#include<bits/stdc++.h>
using namespace std;

int N,M;
string s[10000];
int arr[300][300][3];			// 0 not go			1 $ 	2 $wall		3 @&$    -1 cant go
int can111 = 0,can222=0;
int xx[4] = {0,1,0,-1};
int yy[4] = {1,0,-1,0};
// left 0 right 1
// not 0 go 1

int dfs(int x,int y,int LR){
	//printf("%d %d %d %d\n",x,y,LR,breakwall);
	if( arr[x][y][LR] != 0 ){
		return arr[x][y][LR];
	}
	
	else if( s[x][y] == '$' ){
		arr[x][y][LR] = 1;
		return arr[x][y][LR];
	}
	
	
	int a1=0,a2=0,b1=0,b2=0,b3=0,c1=0,c2=0,c3=0,ccc=0;
	
	if( x+1 < N && s[x+1][y] != '#' ){
		ccc=1;
		a1 = dfs(x+1,y,LR);
	}
	
	if(ccc==0){
		if( LR == 0 ){
			
			if( y-1 >= 0 && s[x][y-1] == '.'){
				b1 = dfs(x,y-1,LR);
			}
			if( y == 0 ){
				b3 = dfs(x,y,1);
			}
			
		}
		
		if( LR == 1 ){
			
			if( y+1 < M && s[x][y+1] == '.'){
				c1 = dfs(x,y+1,LR);
			}
			if( y == M-1 ){
				c3 = dfs(x,y,0);
			}
			
		}
	}
	
	int check1=0,check2=0,check3=0;
	if(a1==1 || a2==1 || b1==1 || b2==1 || b3==1 || c1==1 || c2==1 || c3==1){
		check1 = 1;
	}


	if(check1){
		arr[x][y][LR] = 1;
		return arr[x][y][LR];
	}

	
	arr[x][y][LR] = -1;
	return arr[x][y][LR];
	
}

int main(){
	int count1 = 0,count2 = 0;
	cin >> M >> N;
	string sss;
	for(int i=0;i<M;i++){
		sss.push_back('.');
	}
	for(int i=0;i<N*2;i+=2){
		s[i] = sss;
		cin >> s[i+1];
	}
	/*cout << "print new\n";
	
	for(int i=1;i<=N*2;i+=2){
		cout << s[i] << "\n";
	}*/
	//cout<< s[81][35] << "\n";
	
	N *= 2;
	for(int i=0;i<M;i++){
		//printf("0 %d LR = %d\n",i,0);
		int check = 0;
		check = dfs(0,i,0);
		if(check==1){
			count1++;
		}

		check = 0;
		check = dfs(0,i,1);
		if(check==1){
			count1++;
		}
		
	}
	//cout << count1 << " " << count2 << "\n";
	
	int maxx = -1;
	for(int bi=1;bi<N;bi+=2){
		for(int bj=0;bj<M;bj++){
			
			if( s[bi][bj] != '#' )	continue;
			
			s[bi][bj] = '.';
			
			
			for(int reseti=0;reseti<N;reseti++){
				for(int resetj=0;resetj<M;resetj++){
					for(int kk=0;kk<3;kk++){
						
							arr[reseti][resetj][kk] = 0;
					}
				}
			}
			
			int cxxx = 0;
			for(int i=0;i<M;i++){
				//printf("0 %d LR = %d\n",i,0);
				int checkxxx = 0;
				checkxxx = dfs(0,i,0);
				if(checkxxx==1){
					cxxx++;
				}
		
				checkxxx = 0;
				checkxxx = dfs(0,i,1);
				if(checkxxx==1){
					cxxx++;
				}
				
			}
			//cout << cxxx << "\n";
			if(cxxx>maxx)	maxx = cxxx;
			
			
			
			s[bi][bj] = '#';
		}
	}
	//cout << "max = " << maxx << "\n";
	cout << count1 << " " << maxx << "\n";
}

